export function getMsaById<T extends { id: string }>(msas: T[], id: string): T | undefined {
  return msas.find((msa) => msa.id === id);
}

export function getTopPerformingMsas(msas: MSA[], limit = 3): MSA[] {
  return [...msas]
    .filter((msa) => msa.status === "growing")
    .sort((a, b) => b.healthScore - a.healthScore || b.revenue - a.revenue)
    .slice(0, limit);
}

export function getAttentionRequiredMsas(msas: MSA[]): MSA[] {
  return msas
    .filter((msa) => msa.status === "attention-required")
    .sort((a, b) => {
      if (a.status === "attention-required" && b.status !== "attention-required") return -1;
      if (b.status === "attention-required" && a.status !== "attention-required") return 1;
      if (a.healthScore !== b.healthScore) return a.healthScore - b.healthScore;
      return (100 - a.churnRate) - (100 - b.churnRate);
    });
}

export function buildActivationUpdate(
  msaId: string,
  payload: MarketActivationPayload,
  existing: MsaBase,
): MsaBase {
  const data = buildClientActivationData(existing, payload);
  const pg = payload.physicianGroup;
  const agencyCount = Math.max(payload.hhaRelationships.length, 1);

  return {
    ...existing,
    id: msaId,
    name: existing.name,
    state: existing.state,
    population: existing.population,
    revenue: data.estimatedAnnualRevenue,
    status: "new-market",
    conversionRate: 12,
    churnRate: 5.5,
    healthScore: 0,
    onboardingDays: 14,
    operationalMetrics: {
      patients: pg.activeHomeHealthPatients,
      physicianGroups: 1,
      physicians: Math.max(2, Math.round(pg.activeHomeHealthPatients / 120)),
      homeHealthAgencies: agencyCount,
    },
    activatedAt: new Date().toISOString(),
    activatedBy: pg.primaryContactName,
    activationSource: "new_client",
    onboardingStatus: "pending",
    activationPipelineStatus: undefined,
    clientData: data,
  };
}

